public class Kulka {

    private double mass;
    private double x;
    private double v;

    public Kulka(double mass, double x, double v) {
        this.mass = mass;
        this.x = x;
        this.v = v;
    }

    public double getMass() {
        return mass;
    }

    public double getX() {
        return x;
    }

    public double getV() {
        return v;
    }
}
